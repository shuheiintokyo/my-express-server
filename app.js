// app.js
const express = require("express");
const path = require("path");
const cookieParser = require("cookie-parser");
const logger = require("morgan");
const session = require("express-session");
const createError = require("http-errors");
const {
  router: inspectionLoggingRouter,
} = require("./routes/inspection-logging");
const indexRouter = require("./routes/index");
const exportRouter = require("./routes/export");
const weightInspectionRouter = require("./routes/weight-inspection");
const bodyParser = require("body-parser");
const favicon = require("serve-favicon");

const app = express();

// View engine setup
app.set("views", path.join(__dirname, "views"));
app.set("view engine", "ejs");

// Middleware setup
app.use(bodyParser.json());
app.use(bodyParser.urlencoded({ extended: false }));
app.use(logger("dev"));
app.use(express.json());
app.use(express.urlencoded({ extended: false }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, "public")));
app.use(favicon(path.join(__dirname, "public", "favicon.ico")));

// Session configuration
app.use(
  session({
    secret: "your-secret-key",
    resave: false,
    saveUninitialized: true,
    cookie: {
      secure: process.env.NODE_ENV === "production",
      maxAge: 24 * 60 * 60 * 1000, // 24 hours
    },
  })
);

// Routes
// app.use('/api', exportRouter);
app.use("/", indexRouter);
app.use("/export", exportRouter);
app.use("/weight-inspection", weightInspectionRouter);
app.use("/inspection", inspectionLoggingRouter);

// Inspection sheet route
app.get("/inspection-sheet", (req, res) => {
  res.render("inspection-sheet");
});

// 404 handler
app.use((req, res, next) => {
  next(createError(404));
});

// Error handler
app.use((err, req, res, next) => {
  res.locals.message = err.message;
  res.locals.error = req.app.get("env") === "development" ? err : {};
  res.status(err.status || 500);
  res.render("error");
});

module.exports = app;
